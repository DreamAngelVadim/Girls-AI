import re

# Словарь замен для исправления частых опечаток
REPLACEMENTS = {
    # Опечатки в словах
    'тплый': 'теплый',
    'тплой': 'теплый',
    'малако': 'молоко',
    'превед': 'привет',
    'девченки': 'девчонки',
    'чучу': 'Чу Чу',
    'чу-чу': 'Чу Чу',
    'мей': 'Мэй',
    'майя': 'Мэй',
    'вс': 'все',
    'всё': 'все',
    'потомучто': 'потому что',
    'кароче': 'короче',
    'щас': 'сейчас',
    'ща': 'сейчас',
    'ток': 'только',
    'тока': 'только',
    'ниче': 'ничего',
    'ничё': 'ничего',
    'чё': 'что',
    'чо': 'что',
    'ваще': 'вообще',
    'вообщем': 'в общем',
    'вобщем': 'в общем',
    'кстате': 'кстати',
    'кста': 'кстати',
    'патамушта': 'потому что',
    'патаму': 'потому',
    'дратути': 'здравствуйте',
    'драствуйте': 'здравствуйте',
    'здраствуйте': 'здравствуйте',
    'неа': 'нет',
    'ага': 'да',
    'угу': 'да',
    'спс': 'спасибо',
    'пж': 'пожалуйста',
    'пжл': 'пожалуйста',
}

def apply_replacements(text):
    """Применяет все замены к тексту"""
    if not text:
        return text
    
    result = text
    
    # Заменяем целые слова/фразы
    for wrong, correct in REPLACEMENTS.items():
        result = re.sub(r'\b' + re.escape(wrong) + r'\b', correct, result, flags=re.IGNORECASE)
    
    return result

def fix_before_spell(text):
    """Исправляет текст ПЕРЕД передачей в autocorrect"""
    if not text:
        return text
    
    text = text.lower()
    for wrong, correct in REPLACEMENTS.items():
        text = re.sub(r'\b' + re.escape(wrong) + r'\b', correct, text)
    return text