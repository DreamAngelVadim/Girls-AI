# ========== ФУНКЦИЯ ПРЕОБРАЗОВАНИЯ ЧИСЕЛ ==========

def convert_number(num_str):
    """Конвертирует число в слова"""
    try:
        num = int(num_str)
    except ValueError:
        return num_str
    
    # Для чисел 0-99 используем словарь
    if num <= 99:
        return NUM_WORDS.get(num_str, num_str)
    
    # Для чисел 100-999
    if 100 <= num <= 999:
        hundred = num // 100 * 100
        remainder = num % 100
        if remainder == 0:
            return HUNDRED_WORDS.get(str(hundred), str(hundred))
        else:
            remainder_word = convert_number(str(remainder))
            return f"{HUNDRED_WORDS.get(str(hundred), str(hundred))} {remainder_word}"
    
    # Для чисел 1000 и выше
    if num >= 1000:
        thousands = num // 1000
        remainder = num % 1000
        
        # Склонение слова "тысяча"
        if thousands == 1:
            thousand_str = "одна тысяча"
        elif thousands == 2:
            thousand_str = "две тысячи"
        elif 2 < thousands < 5:
            thousand_str = f"{NUM_WORDS.get(str(thousands), str(thousands))} тысячи"
        else:
            thousand_str = f"{convert_number(str(thousands))} тысяч"
        
        if remainder == 0:
            return thousand_str
        else:
            return f"{thousand_str} {convert_number(str(remainder))}"
    
    return num_str

# ========== ФУНКЦИЯ ЗАМЕНЫ ЧИСЕЛ ==========

def replace_numbers(text):
    """Заменяет все цифры на слова для правильного произношения TTS"""
    if not text:
        return text
    
    def replace_match(match):
        """Заменяет найденное число в тексте"""
        num_str = match.group(0)
        
        # Проверяем время в формате HH:MM
        if ':' in num_str:
            parts = num_str.split(':')
            if len(parts) == 2 and parts[0].isdigit() and parts[1].isdigit():
                hour = int(parts[0])
                minute = int(parts[1])
                if hour <= 23 and minute <= 59:
                    hour_word = convert_number(str(hour))
                    minute_word = convert_number(str(minute))
                    return f"{hour_word} {minute_word}"
        
        # Проверяем числа с плавающей точкой
        if '.' in num_str:
            parts = num_str.split('.')
            if len(parts) == 2 and parts[0].isdigit() and parts[1].isdigit():
                integer_part = convert_number(parts[0])
                fractional_part = convert_number(parts[1])
                return f"{integer_part} целых {fractional_part} десятых"
        
        # Обычное число
        return convert_number(num_str)
    
    # Заменяем время в формате HH:MM
    text = re.sub(r'\b\d{1,2}:\d{2}\b', replace_match, text)
    # Заменяем числа с плавающей точкой
    text = re.sub(r'\b\d+\.\d+\b', replace_match, text)
    # Заменяем целые числа
    text = re.sub(r'\b\d+\b', replace_match, text)
    
    return text

# ========== КОНТЕКСТНАЯ ЗАМЕНА ДЛЯ ДВУСМЫСЛЕННЫХ СЛОВ ==========

def contextual_replace(text, adult_mode=False):
    """
    Заменяет двусмысленные слова в зависимости от режима и контекста
    """
    if not text:
        return text
    
    # ===== СЛОВО "ПОЕШЬ" =====
    if "поешь" in text.lower():
        # Определяем контекст по соседним словам
        food_context = ["суп", "кашу", "борщ", "пиццу", "еду", "кушать", "голод", "есть", "салат", "салата"]
        song_context = ["песню", "песня", "спеть", "пою", "поет", "музыка", "мелодия"]
        
        is_food = any(word in text.lower() for word in food_context)
        is_song = any(word in text.lower() for word in song_context)
        
        if is_song:
            text = text.replace("поешь", "споёшь")
        elif is_food:
            text = text.replace("поешь", "покушаешь")
        # Если контекст неопределённый, оставляем как есть
    
    return text

# ========== ОСНОВНАЯ ФУНКЦИЯ ЗАМЕН ==========

def apply_replacements(text, adult_mode=False):
    """Применяет все замены к тексту"""
    if not text:
        return text
    
    result = text
    
    # Стандартные замены
    for wrong, correct in REPLACEMENTS.items():
        result = re.sub(r'\b' + re.escape(wrong) + r'\b', correct, result, flags=re.IGNORECASE)
    
    # Контекстная замена для двусмысленных слов
    result = contextual_replace(result, adult_mode)
    
    return result

