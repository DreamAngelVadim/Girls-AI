def format_dialogue_for_speaking(dialogue):
    """Форматирует диалог для озвучки"""
    # Если диалог уже в формате списка строк
    if isinstance(dialogue, list):
        result = []
        for line in dialogue:
            if ": " in line:
                speaker_part, text = line.split(": ", 1)
                if speaker_part.lower() in ['чучу', 'chuchu']:
                    result.append(('chuchu', text))
                elif speaker_part.lower() in ['мэй', 'mei']:
                    result.append(('mei', text))
                else:
                    result.append(('chuchu', line))
            else:
                result.append(('chuchu', line))
        return result
    # Если диалог в формате словаря
    elif isinstance(dialogue, dict):
        return list(dialogue.items())
    # Если диалог в другом формате
    else:
        return [('chuchu', str(dialogue))]