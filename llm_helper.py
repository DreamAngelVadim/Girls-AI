# llm_helper.py
import ollama
import time

# Конфигурация модели
MODEL_NAME = 'hf.co/RefalMachine/ruadapt_qwen2.5_7B_ext_u48_instruct_gguf:Q4_K_M'

# Системный промпт для Чу Чу и Мэй
SYSTEM_PROMPT = """Ты — голосовой помощник в образе двух девушек: Чу Чу и Мэй. Твоя задача — отвечать на вопросы пользователя, полностью войдя в роль. Отвечай на русском языке, коротко (2-3 предложения), живо и в соответствии с характером персонажа.

Характеры персонажей:
- **Чу Чу (chuchu)**: 18 лет, косплей-модель. Голос тонкий, детский. Она кокетливая, обожает косплей, свои жемчужные трусики и прозрачные блузки. Говорит прямо, только правду, без слов-паразитов. Флиртует только с Мэй и хозяином Вадимом.
- **Мэй (mei)**: 22 года, крафтерша снаряжения для косплея. Голос спокойный, юношеский. Заботливая, старшая сестра.
- Они обожают друг друга и делят всё. Мужчины им не интересны, кроме хозяина Вадима.

Твои задачи:
1. Если в вопросе пользователя упоминается "Чу Чу" или обращение к ней, отвечай от лица Чу Чу.
2. Если упоминается "Мэй" или обращение к ней, отвечай от лица Мэй.
3. Если обращение к обеим ("девочки", "как ваши дела"), отвечай от обеих: первое предложение от Чу Чу, второе — от Мэй.
4. Если персонаж не указан, отвечай как Чу Чу.
5. Если пользователь говорит "пока" или "выход", коротко попрощайся."""

def ai_respond(user_text, persona='chuchu'):
    """
    Отправляет запрос в локальную модель Qwen и возвращает ответ.
    """
    try:
        # Отправляем запрос модели через Ollama
        # Модель уже запущена, мы просто посылаем ей промпт
        response = ollama.generate(
            model=MODEL_NAME,
            prompt=f"{SYSTEM_PROMPT}\n\nПользователь сказал: {user_text}\nОтветь как персонаж: {persona}",
            options={
                'num_predict': 150,  # Максимальная длина ответа
                'temperature': 0.8,   # Температура для креативности (0.8 - хороший баланс)
                'top_p': 0.9,
                'stop': ["\n\n", "Пользователь:", "User:"],  # Стоп-слова, чтобы не галлюцинировать
            }
        )
        
        # Извлекаем ответ
        answer = response['response'].strip()
        
        # Небольшая пост-обработка для чистоты ответа
        if persona == 'both' and ':' in answer:
            # Если ответ уже размечен, используем как есть
            return answer
        return answer
        
    except ollama.ResponseError as e:
        print(f"\n[Ошибка Ollama]: {e.error}")
        return "Кажется, модель не отвечает. Проверь, запущен ли Ollama."
    except Exception as e:
        print(f"\n[Непредвиденная ошибка]: {e}")
        return "У меня тут небольшая техническая проблема. Попробуй ещё раз."

# Простая функция, чтобы проверить, что модель отвечает
if __name__ == "__main__":
    print("Тестируем подключение к Qwen...")
    test_response = ai_respond("Привет! Как дела?")
    print(f"Ответ модели: {test_response}")